int	ft_str_is_printable(char *str)
{
	int	r;
	int	i;

	r = 1;
	i = 0;
	while (str[i])
	{
		if (str[i] < 32 || str[i] == 127)
		{
			r = 0;
		}
		i++;
	}
	return (r);
}
