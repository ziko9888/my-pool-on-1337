char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int				j;
	unsigned int	i;

	j = 0;
	while (dest[j])
	{
		j++;
	}
	i = 0;
	while (src[i] && i < nb)
	{
		dest[j] = src[i];
		i++;
		j++;
	}
	dest[j] = '\0';
	return (dest);
}
