#include <unistd.h>

void	ft(char c)
{
	write(1, &c, 1);
}

int	main(int argc, char *argv[])
{
	int	i;

	i = 0;
	if (argc >= 1)
	{
		while (argv[0][i])
		{
			ft(argv[0][i++]);
		}
	}
	ft('\n');
}
